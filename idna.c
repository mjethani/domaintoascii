  size_t i, l, h;

  l = 0;
  h = sizeof marks / sizeof marks[0] - 1;

  do {
    i = (l + h) >> 1;

    if (cp < marks[i]) {
      if (h == 0)
        break;

      h = i - 1;
    } else if (cp > marks[i])
      l = i + 1;
    else
      return 1;
  } while (l <= h);

  return 0;
